    private void cargar_modelo_tabla(){
        modelo_tabla.addColumn("Nombre");
        modelo_tabla.addColumn("Edad");
        modelo_tabla.addColumn("Peso (kg)");
        modelo_tabla.addColumn("Estatura (cm)");
        
        leer_binario();
    }
    
    private void leer_binario(){
        Clase_atleta atleta;
        String nombre, edad, peso, estatura;
        
        try {
            FileInputStream archivo = new FileInputStream("Registro_atletas.bin");
            ObjectInputStream lectura = new ObjectInputStream(archivo);
            
            while(true){
                    atleta =(Clase_atleta)lectura.readObject();
                    nombre = atleta.getNombre();
                    edad = String.valueOf(atleta.getEdad());
                    peso = String.valueOf(atleta.getPeso());
                    estatura = String.valueOf(atleta.getEstatura());
                    
                    String [] fila = {nombre, edad , peso , estatura};
                    
                    modelo_tabla.addRow(fila);
                    }
            
        }catch(EOFException ex){
            return; //se llego al final del archivo
        }catch (FileNotFoundException ex) {
            System.err.println("Error, "+ex);
        }catch (IOException ex) {
            System.err.println("Error, "+ex);
        }catch (ClassNotFoundException ex) {
            System.err.println("Error, "+ex);
        }
    }
